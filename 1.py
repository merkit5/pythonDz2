a = float(input('Введите первое значение: '))
b = float(input('Введите второе значение: '))
print("Сумма чисел равна: ", a + b)
print("Разность чисел равна: ", a - b)
print("Произведение чисел равно: ", a * b)
print("Частное чисел равно: ", a / b)
print("Возведение в степень равно: ", a**b)
print("Деление по модулю равно: ", a % b)
print("Целочисленное деление равно: ", a // b)

